#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	int				*str1;
	int				*str2;
	unsigned int	i;

	str1 = (int *) s1;
	str2 = (int *) s2;
	i = 0;
	while (i < n)
	{
		if (str1[i] == str2[i])
			i++;
		else
			return (str1[i] - str2[i]);
	}
	return (0);
}
